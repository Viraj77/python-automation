# Lists
# This script demonstrates how to use lists in Python.

# Creating a list
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Accessing elements
print("First fruit:", fruits[0])

# Adding an element
fruits.append("orange")
print("After adding orange:", fruits)

# Looping through a list
for fruit in fruits:
    print("I like", fruit)
